package exercicios.poo.correcao.paciente;

public class Paciente {

	String Nome;
	int idade;
	String cpf;
	String AlaHospitalar;
	
	public String getNome() {
		return Nome;
	}
	public void setNome(String nome) {
		Nome = nome;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	public String getCpf() {
		return cpf;
	}
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	public String getAlaHospitalar() {
		return AlaHospitalar;
	}
	public void setAlaHospitalar(String alaHospitalar) {
		AlaHospitalar = alaHospitalar;
	}
	
	
}
